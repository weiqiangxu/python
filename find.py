def down_load_image(book_name,author_name,book_id):
	url = 'http://www.jingyu.com/search/?kw='+str(book_name)
	r = requests.get(url)
	soup = BeautifulSoup(r.text, "html.parser")
	# 查找节点
	novel_list = soup.find('div','result-list').find_all("div", "novel")
	for novel in novel_list:
		img = novel.find('div','novel-cover').find('img').get('data-src')  # 封面
		img = img.replace('dm/120_160_100','')
		b_name = novel.find('div','novel-info').find('div','title').find('a').get_text()  # 书名
		a_name = novel.find('div','novel-info').find('div','brief').find('a').get_text()  # 作者名称
		if book_name == b_name and a_name == author_name: 
			save_img(img,book_id)
	print(book_name+' checked')			


